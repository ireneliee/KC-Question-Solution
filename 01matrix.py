
from collections import deque
from typing import List


class Solution:
    def updateMatrix(self, mat: List[List[int]]) -> List[List[int]]:
        result = []

        # populating the queue
        for i in range(len(mat)):
            result.append([])
            for j in range(len(mat[i])):
                result[i].append(214748364)

        q = deque()

        for i in range(len(mat)):
            for j in range(len(mat[i])):
                if mat[i][j] == 0:
                    q.appendleft([i, j])
                    result[i][j] = 0

        x_edge = [1, -1, 0, 0]
        y_edge = [0, 0, 1, -1]

        while q:
            popped = q.pop()
            for i in range(len(x_edge)):
                new_r = popped[0] + x_edge[i]
                new_c = popped[1] + y_edge[i]

                if 0 <= new_r < len(mat) and 0 <= new_c < len(mat[0]):
                    if result[new_r][new_c] > result[popped[0]][popped[1]] + 1:
                        result[new_r][new_c] = result[popped[0]][popped[1]] + 1
                        q.appendleft([new_r, new_c])

        return result


